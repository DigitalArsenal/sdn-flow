import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, "..");

const assetEntries = [
  { route: "/", file: "docs/index.html", type: "text/html; charset=utf-8" },
  { route: "/index.html", file: "docs/index.html", type: "text/html; charset=utf-8" },
  { route: "/css/style.css", file: "docs/css/style.css", type: "text/css; charset=utf-8" },
  { route: "/js/app.mjs", file: "docs/js/app.mjs", type: "text/javascript; charset=utf-8" },
  { route: "/js/flow-canvas.mjs", file: "docs/js/flow-canvas.mjs", type: "text/javascript; charset=utf-8" },
  { route: "/js/flow-model.mjs", file: "docs/js/flow-model.mjs", type: "text/javascript; charset=utf-8" },
  { route: "/js/editor-panel.mjs", file: "docs/js/editor-panel.mjs", type: "text/javascript; charset=utf-8" },
  {
    route: "/js/workers/emception.worker.js",
    file: "docs/js/workers/emception.worker.js",
    type: "text/javascript; charset=utf-8",
  },
  {
    route: "/js/workers/pyodide.worker.js",
    file: "docs/js/workers/pyodide.worker.js",
    type: "text/javascript; charset=utf-8",
  },
];

async function build() {
  const embeddedAssets = {};
  for (const entry of assetEntries) {
    const absolutePath = path.join(repoRoot, entry.file);
    const body = await fs.readFile(absolutePath, "utf8");
    embeddedAssets[entry.route] = {
      contentType: entry.type,
      body,
    };
  }

  const outputPath = path.join(repoRoot, "src/editor/embeddedAssets.generated.js");
  const output = `// This file is generated by scripts/build-editor-assets.mjs.\n` +
    `// Do not edit by hand.\n\n` +
    `export const EmbeddedEditorAssets = ${JSON.stringify(embeddedAssets, null, 2)};\n\n` +
    `export default EmbeddedEditorAssets;\n`;

  await fs.mkdir(path.dirname(outputPath), { recursive: true });
  await fs.writeFile(outputPath, output, "utf8");
  console.log(`Wrote ${path.relative(repoRoot, outputPath)}`);
}

await build();
